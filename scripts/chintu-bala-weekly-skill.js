#!/usr/bin/env node
'use strict';

// =============================================================================
// chintu-bala-weekly-skill.js -- C73
// -----------------------------------------------------------------------------
// Chintu skill: reads the local BALA export file and returns a formatted
// 7-day health digest, suitable for a Telegram reply or CLI output.
//
// Key contracts:
//   getBALAWeeklyDigest(exportPath?) -> {
//     ok      : boolean
//     text    : string    -- Telegram-ready formatted message
//     reason? : string    -- if ok:false, why it failed
//     raw?    : object    -- digest data if ok:true
//   }
//
// Hard safety rules:
//   - PURE local file read — no network, no external API, no Telegram calls.
//   - No medical claims, no diagnosis, no risk prediction.
//   - Emergency phrases are never generated by this skill.
//   - BALA safety footer always appended.
//   - Returns graceful {ok:false, text} on any error — never throws to caller.
//   - Health data read-only — this skill NEVER writes to bala-export.json.
//
// Env:
//   CHINTU_BALA_EXPORT_PATH  -- override default bala-export.json location
//
// Usage:
//   node scripts/chintu-bala-weekly-skill.js
// =============================================================================

const fs   = require('fs');
const path = require('path');

const SKILL_ID      = 'chintu.balaWeeklySkill';
const SKILL_VERSION = 'C73';
const DIGEST_DAYS   = 7;
const DIGEST_MIN    = 2;

const REPO_ROOT     = path.resolve(__dirname, '..');
const DEFAULT_EXPORT_PATH = path.join(REPO_ROOT, 'bala-export.json');

const SAFETY_FOOTER =
  'BALA is a health-awareness companion — not a medical device. ' +
  'Signals are for awareness only. Consult your doctor for health decisions.';

// ─── File loading ────────────────────────────────────────────────────────────

function resolveExportPath(override) {
  if (override) return override;
  return process.env.CHINTU_BALA_EXPORT_PATH || DEFAULT_EXPORT_PATH;
}

function loadBALAExport(filePath) {
  if (!fs.existsSync(filePath)) {
    return { ok: false, reason: 'export_not_found', detail: filePath };
  }
  var raw;
  try {
    raw = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
  } catch (e) {
    return { ok: false, reason: 'parse_error', detail: e.message };
  }
  // Format A — B62 simple export: { exportVersion:2, today:{...}, history:[...] }
  if (raw.exportVersion === 2 && raw.today !== undefined) {
    return { ok: true, history: Array.isArray(raw.history) ? raw.history : [], today: raw.today };
  }
  // Format B — full BALA backup: { format:'bala-export-v1', data:{ health:{...} } }
  if (raw.format && raw.data && raw.data.health) {
    var h = raw.data.health;
    return { ok: true, history: Array.isArray(h.history) ? h.history : [], today: h };
  }
  // Format C — bare health object with history array
  if (Array.isArray(raw.history)) {
    return { ok: true, history: raw.history, today: raw };
  }
  return { ok: false, reason: 'unknown_format' };
}

// ─── Digest computation ──────────────────────────────────────────────────────

function getLastNDays(history, n) {
  if (!Array.isArray(history) || n <= 0) return [];
  return history
    .filter(function (e) { return e && e.date; })
    .sort(function (a, b) { return a.date < b.date ? -1 : 1; })
    .slice(-n);
}

function numAvg(entries, field) {
  var vals = entries.map(function (e) { return parseFloat(e[field]); }).filter(function (v) { return !isNaN(v) && v > 0; });
  if (!vals.length) return null;
  return Math.round((vals.reduce(function (s, v) { return s + v; }, 0) / vals.length) * 10) / 10;
}

function trendDir(entries, field) {
  var vals = entries.map(function (e) { return parseFloat(e[field]); }).filter(function (v) { return !isNaN(v) && v > 0; });
  if (vals.length < 2) return 'stable';
  var mid = Math.ceil(vals.length / 2);
  var avgA = vals.slice(0, mid).reduce(function (s, v) { return s + v; }, 0) / mid;
  var b    = vals.slice(Math.floor(vals.length / 2));
  var avgB = b.reduce(function (s, v) { return s + v; }, 0) / b.length;
  return Math.abs(avgB - avgA) < 2 ? 'stable' : avgB > avgA ? '↑' : '↓';
}

function bestDay(entries) {
  if (!entries.length) return null;
  return entries.reduce(function (b, e) {
    var s = parseFloat(e.balaScore);
    return (!isNaN(s) && (b === null || s > b.score)) ? { date: e.date, score: s } : b;
  }, null);
}

function fmtDate(ds) {
  if (!ds) return '?';
  var p = ds.split('-');
  var mo = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
  return (mo[parseInt(p[1], 10) - 1] || p[1]) + ' ' + parseInt(p[2], 10);
}

function computeDigest(history) {
  var entries = getLastNDays(history, DIGEST_DAYS);
  if (entries.length < DIGEST_MIN) {
    return { ok: false, reason: 'not_enough_data', entriesFound: entries.length };
  }
  var avgScore   = numAvg(entries, 'balaScore');
  var avgSleep   = numAvg(entries, 'sleepHours');
  var avgHRV     = numAvg(entries, 'hrv');
  var avgSteps   = numAvg(entries, 'steps');
  var sleepTrend = trendDir(entries, 'sleepHours');
  var stepsTrend = trendDir(entries, 'steps');
  var hrvTrend   = trendDir(entries, 'hrv');
  var best       = bestDay(entries);
  return {
    ok: true,
    entriesFound: entries.length,
    avgScore: avgScore,
    avgSleep: avgSleep,
    avgHRV: avgHRV,
    avgSteps: avgSteps,
    sleepTrend: sleepTrend,
    stepsTrend: stepsTrend,
    hrvTrend: hrvTrend,
    best: best
  };
}

// ─── Telegram text formatter ─────────────────────────────────────────────────

function formatHeadline(avgScore) {
  if (avgScore === null)  return 'Your body has been sending signals this week.';
  if (avgScore >= 80)     return 'Great week — your signals look strong.';
  if (avgScore >= 60)     return 'Solid week — keep listening to your body.';
  return 'Your body is talking — check in more this week.';
}

function formatDigestText(d) {
  var lines = [];
  lines.push('📊 *Your BALA 7-Day Digest*');
  lines.push('');
  lines.push(formatHeadline(d.avgScore));
  lines.push('');

  if (d.avgScore !== null) {
    lines.push('🏅 Avg BALA Score: *' + d.avgScore + ' / 100*');
  }
  if (d.best) {
    lines.push('✨ Best day: ' + fmtDate(d.best.date) + ' (score ' + d.best.score + ')');
  }
  lines.push('');
  lines.push('*Signal trends (7 days):*');
  lines.push('• Sleep ' + d.sleepTrend + (d.avgSleep !== null ? '  avg ' + d.avgSleep + ' hrs' : ''));
  lines.push('• Steps ' + d.stepsTrend + (d.avgSteps !== null ? '  avg ' + Math.round(d.avgSteps) : ''));
  lines.push('• HRV   ' + d.hrvTrend   + (d.avgHRV   !== null ? '  avg ' + d.avgHRV + ' ms' : ''));
  lines.push('');
  lines.push('_' + SAFETY_FOOTER + '_');
  return lines.join('\n');
}

// ─── Main export ─────────────────────────────────────────────────────────────

function getBALAWeeklyDigest(exportPathOverride) {
  var filePath = resolveExportPath(exportPathOverride);
  var loaded   = loadBALAExport(filePath);

  if (!loaded.ok) {
    var friendlyReason = {
      export_not_found: 'BALA export not found. In BALA: go to Timeline → Export JSON, save as bala-export.json, then re-run.',
      parse_error:      'Could not read BALA export (JSON parse error). Try re-exporting from BALA.',
      unknown_format:   'Unrecognised BALA export format. Try re-exporting from the BALA app.',
    }[loaded.reason] || ('Export error: ' + loaded.reason);

    return {
      ok:   false,
      text: '📊 *BALA Digest* — not available\n\n' + friendlyReason + '\n\n_' + SAFETY_FOOTER + '_',
      reason: loaded.reason,
      skillId: SKILL_ID,
      version: SKILL_VERSION,
    };
  }

  var digest = computeDigest(loaded.history);

  if (!digest.ok) {
    return {
      ok:   false,
      text: '📊 *BALA Digest*\n\nNot enough check-in data yet (found ' + digest.entriesFound + ' days, need ' + DIGEST_MIN + '+). Keep checking in and your 7-day digest will appear here.\n\n_' + SAFETY_FOOTER + '_',
      reason: digest.reason,
      skillId: SKILL_ID,
      version: SKILL_VERSION,
    };
  }

  return {
    ok:   true,
    text: formatDigestText(digest),
    raw:  digest,
    skillId: SKILL_ID,
    version: SKILL_VERSION,
  };
}

// ─── CLI entry ───────────────────────────────────────────────────────────────

if (require.main === module) {
  var result = getBALAWeeklyDigest();
  console.log(result.text);
  console.log('');
  console.log('--- skill: ' + result.skillId + ' v' + result.version + ' | ok: ' + result.ok + (result.reason ? ' | reason: ' + result.reason : ''));
}

module.exports = {
  getBALAWeeklyDigest: getBALAWeeklyDigest,
  computeDigest: computeDigest,
  loadBALAExport: loadBALAExport,
  formatDigestText: formatDigestText,
  formatHeadline: formatHeadline,
  fmtDate: fmtDate,
  getLastNDays: getLastNDays,
  numAvg: numAvg,
  trendDir: trendDir,
  bestDay: bestDay,
  resolveExportPath: resolveExportPath,
  SKILL_ID: SKILL_ID,
  SKILL_VERSION: SKILL_VERSION,
  SAFETY_FOOTER: SAFETY_FOOTER,
  DIGEST_DAYS: DIGEST_DAYS,
  DIGEST_MIN: DIGEST_MIN,
};
