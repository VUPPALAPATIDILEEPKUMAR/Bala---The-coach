'use strict';
// =============================================================================
// BALA-B49 Doctor-Ready Export Summary Engine
// Pure logic. No DOM. No network. No AI inference.
// Generates a plain-text wellness snapshot suitable for sharing with a doctor.
//
// Safety rules:
//   - Output is a PERSONAL WELLNESS LOG, not a medical record or diagnosis
//   - Disclaimer must always appear in the generated text
//   - No outcome claims ("your HRV means X", "this indicates Y")
//   - No emergency guidance in the summary itself (separate gate handles that)
//   - All data is local; engine does not read/write storage directly
// =============================================================================

var DISCLAIMER =
  'IMPORTANT: This is a personal wellness log generated by BALA. ' +
  'It is not a medical record, diagnosis, or treatment recommendation. ' +
  'Share it with your doctor as background context only. ' +
  'For urgent symptoms, contact emergency services immediately.';

// ---------------------------------------------------------------------------
// average — returns rounded average of a numeric array, or null if empty
// ---------------------------------------------------------------------------
function average(values) {
  var nums = (Array.isArray(values) ? values : []).filter(function(v) {
    return typeof v === 'number' && isFinite(v);
  });
  if (nums.length === 0) return null;
  return Math.round((nums.reduce(function(a, b) { return a + b; }, 0) / nums.length) * 10) / 10;
}

// ---------------------------------------------------------------------------
// formatDate — "YYYY-MM-DD" → "DD Mon YYYY" (e.g. "14 Jun 2026")
// Returns the original string if it can't be parsed.
// ---------------------------------------------------------------------------
var MONTHS = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
function formatDate(iso) {
  if (typeof iso !== 'string') return '';
  var parts = iso.split('-');
  if (parts.length !== 3) return iso;
  var y = parseInt(parts[0], 10);
  var m = parseInt(parts[1], 10) - 1;
  var d = parseInt(parts[2], 10);
  if (isNaN(y) || isNaN(m) || isNaN(d) || m < 0 || m > 11) return iso;
  return d + ' ' + MONTHS[m] + ' ' + y;
}

// ---------------------------------------------------------------------------
// buildMetricsSummary — returns plain-text block from history array
// history: [{date, sleep, hrv, rhr, spo2, steps, exercise}, ...]
// ---------------------------------------------------------------------------
function buildMetricsSummary(history) {
  if (!Array.isArray(history) || history.length === 0) {
    return 'No check-in data recorded yet.';
  }

  // Sort by date ascending
  var sorted = history.slice().sort(function(a, b) {
    return String(a.date || '').localeCompare(String(b.date || ''));
  });

  var first = sorted[0].date;
  var last  = sorted[sorted.length - 1].date;
  var count = sorted.length;

  var sleeps    = sorted.map(function(h) { return h.sleep; });
  var hrvs      = sorted.map(function(h) { return h.hrv; });
  var rhrs      = sorted.map(function(h) { return h.rhr; });
  var spo2s     = sorted.map(function(h) { return h.spo2; });
  var stepsList = sorted.map(function(h) { return h.steps; });

  var lines = [];
  lines.push('Check-in period: ' + formatDate(first) + ' to ' + formatDate(last));
  lines.push('Total check-ins: ' + count);
  lines.push('');
  lines.push('Averages across check-ins:');

  var avgSleep = average(sleeps);
  if (avgSleep !== null) lines.push('  Sleep duration: ' + avgSleep + ' hrs/night');

  var avgHrv = average(hrvs);
  if (avgHrv !== null) lines.push('  HRV (heart-rate variability): ' + avgHrv + ' ms');

  var avgRhr = average(rhrs);
  if (avgRhr !== null) lines.push('  Resting heart rate: ' + avgRhr + ' bpm');

  var avgSpo2 = average(spo2s);
  if (avgSpo2 !== null) lines.push('  Blood oxygen (SpO₂): ' + avgSpo2 + '%');

  var avgSteps = average(stepsList);
  if (avgSteps !== null) lines.push('  Daily steps: ' + Math.round(avgSteps));

  if (lines[lines.length - 1] === 'Averages across check-ins:') {
    lines.push('  (No numeric signals recorded)');
  }

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// buildSymptomSection — summarises the nudge log (last N days)
// nudgeLog: [{date, chipId}, ...] from BALA-B48 symptom nudge
// days: number of days to look back (default 14)
// ---------------------------------------------------------------------------
function buildSymptomSection(nudgeLog, days) {
  var lookback = typeof days === 'number' && days > 0 ? days : 14;
  if (!Array.isArray(nudgeLog) || nudgeLog.length === 0) {
    return 'No symptom check-ins recorded in the last ' + lookback + ' days.';
  }

  // Filter to last N days by date string (lexicographic comparison is safe for ISO dates)
  var cutoff = new Date(Date.now() - lookback * 24 * 60 * 60 * 1000).toISOString().slice(0, 10);
  var recent = nudgeLog.filter(function(e) {
    return typeof e.date === 'string' && e.date >= cutoff && e.chipId !== 'skip';
  });

  if (recent.length === 0) {
    return 'No symptom check-ins recorded in the last ' + lookback + ' days.';
  }

  // Count by chipId
  var counts = {};
  recent.forEach(function(e) {
    counts[e.chipId] = (counts[e.chipId] || 0) + 1;
  });

  var lines = ['Body signal check-ins (last ' + lookback + ' days):'];
  Object.keys(counts).sort().forEach(function(id) {
    lines.push('  ' + id + ': ' + counts[id] + ' day' + (counts[id] > 1 ? 's' : ''));
  });

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// buildFocusSection — summarises the weekly focus log (last N weeks)
// focusLog: [{date, text, tried}] from BALA-B46
// weeks: number of weeks to look back (default 4)
// ---------------------------------------------------------------------------
function buildFocusSection(focusLog, weeks) {
  var lookback = typeof weeks === 'number' && weeks > 0 ? weeks : 4;
  if (!Array.isArray(focusLog) || focusLog.length === 0) {
    return 'No weekly focus entries recorded.';
  }

  var cutoff = new Date(Date.now() - lookback * 7 * 24 * 60 * 60 * 1000).toISOString().slice(0, 10);
  var recent = focusLog.filter(function(e) {
    return typeof e.date === 'string' && e.date >= cutoff;
  });

  if (recent.length === 0) {
    return 'No weekly focus entries in the last ' + lookback + ' weeks.';
  }

  var lines = ['Weekly focus log (last ' + lookback + ' weeks — ' + recent.length + ' entries):'];
  // Show last 7 entries at most to keep summary readable
  recent.slice(-7).forEach(function(e) {
    var tried = e.tried === true ? ' ✓ tried' : e.tried === false ? ' – skipped' : '';
    lines.push('  ' + formatDate(e.date) + ': ' + (e.text || '(no text)') + tried);
  });

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// generateSummary — main entry: returns the full plain-text export string
//
// opts:
//   history    — array of check-in snapshots (required)
//   nudgeLog   — array of symptom nudge entries (optional)
//   focusLog   — array of weekly focus log entries (optional)
//   generatedAt — ISO string timestamp (optional, defaults to now)
// ---------------------------------------------------------------------------
function generateSummary(opts) {
  var o = opts || {};
  var history   = Array.isArray(o.history)  ? o.history  : [];
  var nudgeLog  = Array.isArray(o.nudgeLog) ? o.nudgeLog : [];
  var focusLog  = Array.isArray(o.focusLog) ? o.focusLog : [];
  var ts        = typeof o.generatedAt === 'string' ? o.generatedAt : new Date().toISOString();

  var sep = '─'.repeat(52);
  var parts = [];

  parts.push('BALA Personal Wellness Log');
  parts.push('Generated: ' + ts.slice(0, 10) + ' (local device)');
  parts.push(sep);
  parts.push('');
  parts.push(buildMetricsSummary(history));
  parts.push('');
  parts.push(sep);
  parts.push('');
  parts.push(buildSymptomSection(nudgeLog));
  parts.push('');
  parts.push(sep);
  parts.push('');
  parts.push(buildFocusSection(focusLog));
  parts.push('');
  parts.push(sep);
  parts.push('');
  parts.push(DISCLAIMER);

  return parts.join('\n');
}

// ---------------------------------------------------------------------------
module.exports = {
  average,
  formatDate,
  buildMetricsSummary,
  buildSymptomSection,
  buildFocusSection,
  generateSummary,
  DISCLAIMER,
};
