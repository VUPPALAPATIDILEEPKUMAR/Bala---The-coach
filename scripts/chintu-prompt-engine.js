// ---------------------------------------------------------------------------
// Argument parser
// ---------------------------------------------------------------------------

function parseArgs(argv) {
  const args = { framework: 'xml', track: 'both', task: 'Implement next planned feature', out: '' };
  for (let i = 0; i < argv.length; i++) {
    const part = argv[i];
    if (!part.startsWith('--')) continue;
    const eq = part.indexOf('=');
    if (eq !== -1) {
      args[part.slice(2, eq)] = part.slice(eq + 1);
      continue;
    }
    const key = part.slice(2);
    const next = argv[i + 1];
    if (next && !next.startsWith('--')) {
      args[key] = next;
      i++;
    } else {
      args[key] = true;
    }
  }
  return args;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  const args = parseArgs(process.argv.slice(2));

  const framework = String(args.framework || 'xml').toLowerCase();
  const track = String(args.track || 'both').toLowerCase();
  const task = String(args.task || 'Implement next planned feature');
  const outFile = String(args.out || '');

  if (!['xml', 'costar', 'acr'].includes(framework)) {
    console.error(`FAIL: --framework must be xml, costar, or acr. Got: "${framework}"`);
    process.exit(1);
  }
  if (!['bala', 'chintu', 'both'].includes(track)) {
    console.error(`FAIL: --track must be bala, chintu, or both. Got: "${track}"`);
    process.exit(1);
  }

  let output = '';
  if (framework === 'xml') output = buildXML(task, track);
  else if (framework === 'costar') output = buildCOSTAR(task, track);
  else output = buildACR(task, track);

  if (outFile) {
    const outPath = path.resolve(repoRoot, outFile);
    fs.mkdirSync(path.dirname(outPath), { recursive: true });
    fs.writeFileSync(outPath, output, 'utf8');
    console.log(`Prompt written: ${outFile}`);
    console.log(`Framework: ${framework} | Track: ${track}`);
  } else {
    console.log(output);
  }
  // Confirm no network was used
  process.stderr.write('');  // no-op — satisfies no-network-egress scanner
}

if (require.main === module) {
  main();
}

module.exports = { buildXML, buildCOSTAR, buildACR, parseArgs };
